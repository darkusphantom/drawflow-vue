const sum = (a, b) => a + b;
const subs = (a, b) => a - b;
const mult = (a, b) => a * b;
const div = (a, b) => a / b;
const mod = (a, b) => a % b;
const power = (a, b) => a ** b;
const square = (value) => Math.sqrt(value);
const exponential = (value) => Math.exp(value);
const logarithm = (value) => Math.log(value);
const absolute = (value) => Math.abs(value);
const round = (value) => Math.round(value);

export {
  sum,
  subs,
  mult,
  div,
  mod,
  power,
  square,
  exponential,
  logarithm,
  absolute,
  round,
};
